#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*current;

	if (!lst || !new)
		return (NULL);
	
	while ((*lst)->next)
	{
		lst = (*lst)->next;
	}
	new->next = lst;

}
